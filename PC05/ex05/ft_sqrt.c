int	ft_sqrt(int nb)
{
	int i;
	int n;

	n = 0;
	i = 1;
	while (n / i < nb / i)
	{
		n = i * i;
		i++;
	}
	if (n == nb)
		return (i - 1);
	else
		return (0);
}
