int	ft_find_next_prime(int nb)
{
	int i;

	i = 3;
	if (nb < 2)
		return (2);
	while (i * i <= nb)
	{
		if (nb % i != 0)
			i += 2;
		else
		{
			i = 3;
			nb++;
		}
	}
	return (nb);
}
