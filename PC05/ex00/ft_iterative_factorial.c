int		ft_iterative_factorial(int nb)
{
	int		i;
	int		n;

	i = 1;
	n = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (i < n)
	{
		nb = nb * (n - i);
		i++;
	}
	return (nb);
}
