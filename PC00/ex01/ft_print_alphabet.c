#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char i;

	i = 96;
	while (i < 122)
	{
		i++;
		ft_putchar(i);
	}
}
